package dam.iesvdc.teoria;

public class Variables1 {
	public static void main ( String [] args) {
		//usando el operando preincremento ++a
	int variable1 = 5;
	System.out.println( " Valor inicial de la variable 1: " + variable1);
	System.out.println( " Valor incrementado de la variable 1: " + ++variable1);
		//usando el postincremento a++
	int variable2 = 5;
	System.out.println( " Valor inicial de la variable 2: " + variable2);
	System.out.println( " Valor incrementado de la variable 2: " + variable2++);
	System.out.println( " Valor inicial de la variable 2: " + variable2);
	}
}
